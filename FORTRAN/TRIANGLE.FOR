      SUBROUTINE triangle(x,y,bb,cc,ar2)
C
C ---------------------------------------------------------------------
C I                           TRIANGLE                                I
C I								      I
C I   FELEMA vs 1.0		  DJB		      14 / Mar / 1994 I
C ---------------------------------------------------------------------
C
      REAL x(3),y(3),bb(3),cc(3),ar2
      bb(1)=y(2)-y(3)
      bb(2)=y(3)-y(1)
      bb(3)=y(1)-y(2)
      cc(1)=x(3)-x(2)
      cc(2)=x(1)-x(3)
      cc(3)=x(2)-x(1)
      ar2=x(2)*y(3)+x(3)*y(1)+x(1)*y(2)-x(2)*y(1)
     + -x(3)*y(2)-x(1)*y(3)
      RETURN
      END
